const I18N = { ru: RU, en: EN };

const Ctx = createContext<{ language: UiLanguage; setLanguage: (v: UiLanguage) => void; t: (key: string, fallback?: string) => string }>({
  language: "ru",
  setLanguage: () => {},
  t: (key, fallback) => fallback ?? key,
});

export function I18nProvider({ initialLanguage = "ru", children }: { initialLanguage?: UiLanguage; children: ReactNode }) {
  const [language, setLanguage] = useState<UiLanguage>(initialLanguage);
  const value = useMemo(() => ({
    language,
    setLanguage,
    t: (key: string, fallback?: string) => I18N[language][key] ?? fallback ?? key,
  }), [language]);
  return <Ctx.Provider value={value}>{children}</Ctx.Provider>;
}

export function useI18n() {
  return useContext(Ctx);
}
